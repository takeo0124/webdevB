<?php
#次の条件に従って、PHPでスコア判定プログラムを作成しなさい。
#①0から100までのランダムな整数を変数$scoreに代入する。
#②$score = rand(0, 100);
#③「スコア: ○○」の形式でスコアを表示する。
#④スコアが80以上なら「優」、60以上80未満なら「良」、それ以外は「可」と表示する。

// 0から100までのランダムな整数を生成
$score = rand(0, 100);

// スコアを表示（ブラウザでの改行を考慮して<br>を使用）
echo "スコア: " . $score . "<br>";

if ($score >= 80):
    echo "優"; // スコアが80以上なら「優」
elseif ($score >= 60):
    echo "良"; // スコアが60以上80未満なら「良」
else:
    echo "可"; // それ以外は「可」
endif; // if文の終わりをendif;で示す
